package com.minty.models;

public class Hero {
    private int id;
    private String name;
    private String power;
    private String weakness;
    private String squadId;

    public Hero(int id, String name, String power, String weakness, String squadId) {
        this.id = id;
        this.name = name;
        this.power = power;
        this.weakness = weakness;
        this.squadId = squadId;
    }



}
